using Xunit;

namespace Code.Tests
{
    public class WildCardTest
    {
        [FactAttribute]
        public void MatchTwice()
        {
            Assert.True(Wildcard.IsMatching("abefcdgiescdfimdf", "ab*cd?i*df"));
        }

        [FactAttribute]
        public void MatchEmpty()
        {
            Assert.False(Wildcard.IsMatching("", ""));
        }

        [FactAttribute]
        public void MatchEmptyStrWithPattern()
        {
            Assert.False(Wildcard.IsMatching("", "a"));
        }

        [FactAttribute]
        public void MatchMiddlePattern()
        {
            Assert.True(Wildcard.IsMatching("ab", "a*b"));
        }

        [FactAttribute]
        public void MatchMiddlePattern2()
        {
            Assert.True(Wildcard.IsMatching("aab", "a*a*b"));
        }

        [FactAttribute]
        public void MatchMiddlePattern3()
        {
            Assert.True(Wildcard.IsMatching("aacab", "a*ab"));
        }

        [FactAttribute]
        public void MatchPrePattern()
        {
            Assert.False(Wildcard.IsMatching("abac", "*a"));
        }

        [FactAttribute]
        public void MatchTailPattern()
        {
            Assert.True(Wildcard.IsMatching("a", "a*"));
        }

        [FactAttribute]
        public void MatchTailPattern2()
        {
            Assert.False(Wildcard.IsMatching("a", "ab*"));
        }

        [FactAttribute]
        public void MatchAllWildcardChar()
        {
            Assert.True(Wildcard.IsMatching("hi", "*?"));
        }
        
        [FactAttribute]
        public void MatchHardcodePattern()
        {
            Assert.False(Wildcard.IsMatching("cab","ab"));            
        }

    }
}